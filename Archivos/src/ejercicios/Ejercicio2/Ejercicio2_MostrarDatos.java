    private void leerDatos() {
        String[] datos = new String[4];
        int contador = 0;
        
        FileInputStream archivo;
        try {
            archivo = new FileInputStream("./files/atletas.bin");
            ObjectInputStream lectura = new ObjectInputStream(archivo);
            
            while (true) { // Recorremos el archivo binario
                Atleta atleta = (Atleta)lectura.readObject();
                datos[0] = atleta.getNombre();
                datos[1] = Integer.toString(atleta.getEdad());
                datos[2] = Double.toString(atleta.getPeso());
                datos[3] = Integer.toString(atleta.getEstatura());
                modelo.addRow(datos);
            }
       
        }catch (EOFException ex) {
            return; // Finalizo de leer el archivo
        } catch (FileNotFoundException ex) {
            Logger.getLogger(Archivos.class.getName()).log(Level.SEVERE, null, ex);
        } catch (IOException ex) {
            Logger.getLogger(Archivos.class.getName()).log(Level.SEVERE, null, ex);
        } catch (ClassNotFoundException ex) {
            Logger.getLogger(Ejercicio2_MostrarDatos.class.getName()).log(Level.SEVERE, null, ex);
        }
        
    }
    
    
    
