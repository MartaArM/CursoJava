    private void br_insertarActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_br_insertarActionPerformed
        String nombre, apellidos, telefono;
        
        nombre = tf_nombre.getText();
        apellidos = tf_apellido.getText();
        telefono = tf_telefono.getText();
        
        Connection conexion = null;
        
        conexion = getConnection();
        try {
            ps = conexion.prepareStatement("INSERT INTO mitabla1(nombre, apellido, telefono) values ('" +
                    nombre + "', '" + apellidos + "','" + telefono + "')");
            int resultado = ps.executeUpdate();
            
            if (resultado >0) {
                JOptionPane.showMessageDialog(null, "Registro insertado.");
            }
            else {
                JOptionPane.showMessageDialog(null, "Registro no insertado.");
            }
            
            conexion.close();
        } catch (SQLException ex) {
            System.err.println("Erros"+ex);
        }
                
        
    }//GEN-LAST:event_br_insertarActionPerformed

