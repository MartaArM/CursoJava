    private void aniadirImagen() {
        ImageIcon image = new ImageIcon("./images/cronometro.png");
        ImageIcon image_scalated = new ImageIcon(image.getImage().
                getScaledInstance(etiqueta_cronometro.getWidth(), etiqueta_cronometro.getHeight(), Image.SCALE_SMOOTH)); //Escalar una imagen
        
        etiqueta_cronometro.setIcon(image_scalated);
    }
    
    private void botonIniciar() {
        ActionListener listener = new ActionListener() {
            @Override
            public void actionPerformed(ActionEvent e) {
                tiempo.start();
            }
        };
        boton_iniciar.addActionListener(listener);
    }
    private void botonPausar() {
        ActionListener listener = new ActionListener() {
            @Override
            public void actionPerformed(ActionEvent e) {
                tiempo.stop();
            }
        };
        boton_pausar.addActionListener(listener);
    }
    
    private void botonDetener() {
        ActionListener listener = new ActionListener() {
            @Override
            public void actionPerformed(ActionEvent e) {
                tiempo.stop();
                horas = 0;
                minutos = 0;
                segundos = 0;
                centesimas = 0;
                etiqueta_tiempo.setText("00:00:00:00");
            }
        };
        boton_detener.addActionListener(listener);
    }
