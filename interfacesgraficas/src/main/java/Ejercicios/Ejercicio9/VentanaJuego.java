    private void inicializarPosiciones() {
        for (int i=0; i<3; i++) {
            for (int j=0; j<3; j++){
                posiciones[i][j] = false;
            }
        }
    }
    
    private void inicializarPulsados() {
        for (int i=0; i<3; i++) {
            for (int j=0; j<3; j++){
                pulsados[i][j] = 0;
            }
        }
    }
    
