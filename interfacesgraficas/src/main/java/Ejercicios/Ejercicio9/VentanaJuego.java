    private void comprobarGanador() {
        /*for (int i=0; i<3; i++) {
            for (int j=0; j<3; j++) {
                System.out.print(pulsados[i][j]);
            }
            System.out.println("");
        }*/
        if (!turno) { // Primer jugador
            if (pulsados[0][0] == 1){
                if ((pulsados[0][1]==1 && pulsados[0][2] == 1) || 
                        (pulsados[1][0]==1 && pulsados[2][0] == 1) ||
                        (pulsados[1][1]==1 && pulsados[2][2] == 1))
                {
                    ganador = 1;
                }
            }
            else if (pulsados[0][1] == 1) {
                if (pulsados[1][1] == 1 && pulsados[2][1] == 1) {
                    ganador = 1;
                }
            }
            else if (pulsados[0][2] == 1){
                if (pulsados[1][2] == 1 && pulsados[2][2]==1) {
                    ganador = 1;
                }
            }
            else if (pulsados[1][0] == 1) {
                if (pulsados[1][1] == 1 && pulsados[1][2] == 1) {
                    ganador = 1;
                }
            }
            else if (pulsados[2][0] == 1) {
                if (pulsados[2][1] == 1 && pulsados[2][2] == 1) {
                    ganador = 1;
                }
            }
        
        }
        else { // Segundo jugador
            if (pulsados[0][0] == 2){
                if ((pulsados[0][1]== 2 && pulsados[0][2] == 2) || 
                        (pulsados[1][0]== 2 && pulsados[2][0] == 2) ||
                        (pulsados[1][1]== 2 && pulsados[2][2] == 2))
                {
                    ganador = 2;
                }
            }
            else if (pulsados[0][1] == 2) {
                if (pulsados[1][1] == 2 && pulsados[2][1] == 2) {
                    ganador = 2;
                }
            }
            else if (pulsados[0][2] == 2){
                if (pulsados[1][2] == 2 && pulsados[2][2]== 2) {
                    ganador = 2;
                }
            }
            else if (pulsados[1][0] == 2) {
                if (pulsados[1][1] == 2 && pulsados[1][2] == 2) {
                    ganador = 2;
                }
            }
            else if (pulsados[2][0] == 2) {
                if (pulsados[2][1] == 2 && pulsados[2][2] == 2) {
                    ganador = 2;
                }
            }
        }
    }
    
