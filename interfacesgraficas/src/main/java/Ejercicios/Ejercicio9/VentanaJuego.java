    private void boton00ActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_boton00ActionPerformed
        // TODO add your handling code here:
        if (!posiciones[0][0]){
            if (!turno){
                dibujarX(boton00);
                pulsados[0][0] = 1;
            }
            else{
                dibujarO(boton00);
                pulsados[0][0] = 2;
            }

            comprobarGanador();
            turno = !turno;
            
            
            if (ganador == 1 ){
                new VentanaGanar(this, false, usuario1).setVisible(true);
            }
            else if (ganador == 2) {
                new VentanaGanar(this, false, usuario2).setVisible(true);
            }
            
            contador++;
            
            if (contador == 9) {
                new VentanaEmpate(this, true).setVisible(true);
            }
        }
        
        posiciones[0][0] = true;
    }//GEN-LAST:event_boton00ActionPerformed

